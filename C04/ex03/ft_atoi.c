int	ft_atoi(char *str)
{
	int	i;
	int	n;
	int	p;

	i = 0;
	n = 1;
	p = 0;
	while ((str[i] >= 8 && str[i] <= 13) || str[i] == 32) 
		i++;
	while (str[i] == '-' || str[i] == '+') 
	{
		if (str[i] == '-')
			n = n * -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		p = p * 10 + (str[i] - 48);
		i++;
	}
	return (p * n);
}
